num1=10
num2=4
print("AND operator  num1 and num 2=", num1 & num2)
#or operator
print("\nnum1 | num 2=", num1 | num2)
#not operator
print("\n~num1 =", ~num1)
#xor operator
print("\nnum1 ^ num 2=", num1 ^ num2)
num1=10
